    WHERE
        (INTERSHO.Cel_3_2G间分组域HO出SucR < 90 AND INTERSHO.YCel_3_2G间分组域HO出SucR <> 0)

    ORDER BY
        INTERSHO.city,
        INTERSHO.Cel_3_2G间分组域HO出SucR


 
 
 