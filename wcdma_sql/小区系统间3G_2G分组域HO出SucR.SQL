            INTERSHO.IS_HHO_ATT_DL_DPCH_PWR_NRT + INTERSHO.IS_HHO_ATT_CPICH_RSCP_NRT +
            INTERSHO.IS_HHO_ATT_CPICH_ECNO_NRT + INTERSHO.IS_HHO_ATT_IM_IMS_NRT +
